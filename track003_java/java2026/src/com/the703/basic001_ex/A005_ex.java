package com.the703.basic001_ex;

public class A005_ex {

	public static void main(String[] args) {
		
		System.out.println("가리랑 가리랑 아라리요 ");
		System.out.print("가리랑 고개를 넘어간다 \n");
		System.out.print("나를 버리고 가시는 님은 \n");
		System.out.printf("십리(%dkm)도 못 가서 발병난다 \n " , 4);
	}

}


/*
>    클래스명 : A005_ex
println, print, printf 3가지 버젼으로 출력

아리랑 아리랑 아라리요      ←  println   
아리랑 고개를 넘어간다      ←  print
나를 버리고 가시는 님은     ←  print  
십리(4km)도 못 가서 발병난다  ←  printf   숫자 출력서식

*/