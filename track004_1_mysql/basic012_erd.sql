■진행1. 정규화  1NF~5NF
STEP1) 정규화?
1. 관계형 데이터모델 - 데이터간 관계에 초점을 둔 모델 
2. 이상(Anomaly) - 공간낭비
   - 삽입, 갱신, 삭제 이상
3. 정규화? 이상을 제거하는것

STEP2) ERD 핵심구성요소
1. 개체(Entity) - 테이블, 개념의 정보단위
2. 속성(Attirubute) - 테이블의 열, 가장 작은 논리적 단위
3. 관계(Relationship) - 외래키, 
                            테이블간에 관계를 외래키 등을 구현해서 사용
예)  emp( FK:deptno ) - dept( PK:deptno)


