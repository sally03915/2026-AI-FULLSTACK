■진행2. ERD
■ ERD(ENTITY  RELATIONSHIP DIAGRAM)    
-> 데이터관계간에 초점을 둔 모델
1. 개체(Entity) - 테이블
2. 속성(Attribute) - 열, 컬럼
3. 관계(RELATIONSHIP) - 외래키


■ 정규화
-> 관계형데이터모델의 이상(삽입, 삭제, 갱신)을 제거하는 것
1NF - 중복항목 없애기
2NF - 종속관계   X(결정자:PK)->Y(종속자)  
         기본키에 완전 함수 종속
3NF - 이행함수 종속제거  BEFORE)X->Y->Z  
		         AFTER)  X->Y/ Y->Z  
        다른후보키에 의존하지 않음!
BCNF - 모든결정자 후보키
4NF   - 다치종속 / 교수2:교재2
5NF   - 조인종속   다:다관계 ->  중간테이블필요



■ ~로 구성되어 있다.   1:1
[ex001]  하나의 A는 하나의 B로 구성되어 있다.
[홍길동]┼───┼[목숨]
[홍길동]┼───┼[주민증]


[ex002]  하나의 A는 여러개의 B로 구성되어 있다.  1:다
한명의 학생은 여러개의 수강내역을가진다.
[홍길동]┼───∈ [JAVA,JSP, SPRING, MYSQL]



